// get an element from the DOM by its ID
var dom = document.getElementById('dom');

// get an element by its tag name
// returns a nodelist (array of dom elements)
document.getElementsByTagName();

// get an element by its class name
// returns a nodelist (array of dom elements)
document.getElementsByClassName();

// allow use of css selectors to find elements in the dom
// returns 1 element like getelementbyid
document.querySelector();
// returns nodelise like tag name and class name
document.querySelectorAll();